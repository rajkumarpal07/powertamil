    @staticmethod
    def _to_tace16(utf8strlist:List) -> List:
        # get tace graphemes in a tacelist
        tacelist = []
        glist = Tace16._to_tace16_graphemes(utf8strlist)
        for grapheme in glist:
            tacelist.append(ALLTACE16.get(grapheme))
        return tacelist

    @staticmethod
    def _to_tace16_graphemes(utf8tamilstr:str) -> list:

        dst = []
        reslist = []

        for codepoint in utf8tamilstr:
            dst.append(codepoint)
        strlen = len(dst)
        i = 0
        # iterate on code points one by one
        while(i < strlen):
            # cp intialized to ASCII Null character
            cp1 = '\0'
            cp2 = '\0'
            cp3 = '\0'
            cp4 = '\0'
            # load code points
            if ((strlen - i) > 3):  # can load four CPs or less
                #print("loading 4 ----------------" + str(i))
                cp1 = dst[i]
                cp2 = dst[i + 1]
                cp3 = dst[i + 2]
                cp4 = dst[i + 3]
                tc4 = TChar.get_TChar4(cp1, cp2, cp3, cp4)
                if (tc4 is not None):
                    #print("hit 4 -4 " + str(i))
                    reslist.append(tc4)
                    i = i + 3
                elif ((tc4 := TChar.get_TChar3(cp1, cp2, cp3)) is not None):
                    #print("hit 4 - 3 " + str(i))
                    reslist.append(tc4)
                    i = i + 2

                elif ((tc4 := TChar.get_TChar2(cp1, cp2)) is not None):
                    #print("hit 4 - 2 " + str(i))
                    reslist.append(tc4)
                    i = i + 1

                elif ((tc4 := TChar.get_TChar1(cp1)) is not None):
                    #print("hit 4 - 1 " + str(i))
                    reslist.append(tc4)
                    i = i + 0

            elif((strlen - i) > 2):  # can load three CPs or less
                #print("loading 3----------------"  + " " + str(i))
                cp1 = dst[i]
                cp2 = dst[i + 1]
                cp3 = dst[i + 2]
                tc3 = TChar.get_TChar3(cp1, cp2, cp3)
                if (tc3 is not None):
                    #print("hit 3 - 3 " + str(i))
                    reslist.append(tc3)
                    i = i + 2

                elif ((tc3 := TChar.get_TChar2(cp1, cp2)) is not None):
                    #print("hit 3 - 2 " + str(i))
                    reslist.append(tc3)
                    i = i + 1

                elif ((tc3 := TChar.get_TChar1(cp1)) is not None):
                    #print("hit 3 - 1 " + str(i))
                    reslist.append(tc3)

            elif((strlen - i) > 1):  # can load remaining 2 CPs or less
                #print("loading 2----------------" + " " + str(i))
                cp1 = dst[i]
                cp2 = dst[i + 1]
                tc2 = TChar.get_TChar2(cp1, cp2)
                if (tc2 is not None):
                    #print("hit 2 - 2 " + str(i))
                    reslist.append(tc2)
                    i = i + 1
                elif ((tc2 := TChar.get_TChar1(cp1)) is not None):
                    #print("hit 2 - 1 " + str(i))
                    reslist.append(tc2)

            elif((strlen - i) == 1):  # can load only one CP
                #print("loading 1----------------" + str(i))
                cp1 = dst[i]
                tc1 = TChar.get_TChar1(cp1)
                if (tc1 is not None):
                    #print("hit 1 - 1 " + str(i))
                    reslist.append(tc1)
            i = i+1
        return reslist

    @staticmethod
    def to_tace16tamil(utf8str:str) -> str:
        inlist = Tace16._to_tace16(utf8str)
        return "".join(inlist)

    @staticmethod
    def _to_utf8tamil(tacestr:str) -> list:        
        utflist = []
        glist = list(tacestr) 
        for grapheme in glist:
            if (ord(grapheme) > MAX_TACE or ord(grapheme) < MIN_TACE):
                raise ValueError(grapheme + ' is not a valid TACE16 codepoint...!')
            print(grapheme)
            utflist.append(ALLUTF8.get(grapheme))
        return utflist

    @staticmethod
    def to_utf8tamil(tacestr:str) -> str:
        inlist = Tace16._to_utf8tamil(tacestr)
        return "".join(inlist)
    

